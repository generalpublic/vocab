def build_entry(word, pos, definition):
    pos_list = [p.strip() for p in pos.replace("/", ",").split(",")]
    pos_map = {
        "noun": "noun", "verb": "verb", "adj": "adjective",
        "adv": "adverb", "prep": "preposition", "conj": "conjunction",
    }
    pos_list = [pos_map.get(p, p) for p in pos_list]
    return {
        "word": word,
        "pos": pos_list,
        "pronunciation": None,
        "definition": definition,
        "register": [],
        "tags": [],
        "enriched": False,
        "examples": [],
        "misuses": [],
        "related": [],
        "triggers": [],
        "date_added": str(date.today()),
    }

def main():
    seen = {}
    words = []
    for word, pos, defn in RAW_WORDS:
        key = word.lower()
        if key not in seen:
            seen[key] = True
            words.append(build_entry(word, pos, defn))

    words.sort(key=lambda w: w["word"].lower())

    db = {
        "meta": {
            "version": 1,
            "last_updated": str(date.today()),
            "word_count": len(words),
        },
        "words": words,
    }

    with open("vocab_db.json", "w", encoding="utf-8") as f:
        json.dump(db, f, indent=2, ensure_ascii=False)

    print(f"Imported {len(words)} unique words into vocab_db.json")

if __name__ == "__main__":
    main()
